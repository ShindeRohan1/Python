print("Demonstration of built in data types")
#python does not support static typing
def main():
    #int obj = 22;//static typing
    iobj = 11#dynamic typing   
    fobj = 3.14
    sobj = "Hello"
    bobj = True

    print(iobj)
    print(fobj)
    print(sobj)
    print(bobj)

    print(type(iobj))
    print(type(fobj))
    print(type(sobj))
    print(type(bobj))



if __name__ == "__main__":
    main()